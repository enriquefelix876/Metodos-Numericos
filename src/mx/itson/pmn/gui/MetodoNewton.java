    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed

       /**
        * Bloque try-catch que comprueba si existe algún error al ingresar los  
        * datos
        */
       try{
       
       tablaNewton.setModel(modelo);
       limpiarTabla(modelo); 
       String[]datos=new String[3];
       
       //Se establece la tolerancia
        double tolerancia = Double.parseDouble(txtTolerancia.getText());
        
        //Valor de la primera aproximación
        double x = Double.parseDouble(txtAproximacion.getText());
        
        double fx;
        double fdx;
        
        //Se realizará todo esto:
        do {          
            fx = evaluar(x);
            fdx = evaluarDerivada(x);
            
            datos[0]=String.valueOf(x);
            datos[1]=String.valueOf(fx);
            datos[2]=String.valueOf(fdx);
            modelo.addRow(datos);
            
            //Se realiza la formula para obtener el nuevo valor de x
            x = x-(fx/fdx);
        
        //Mientras el valor absoluto de x evaluado en la función, sea mayor a la 
        //tolerancia establecida
        } while (Math.abs(evaluar(x))>tolerancia);
        
        txtRaizNewton.setText("Raiz: "+String.valueOf(x));
       
       }catch(Exception e){
       
           JOptionPane.showMessageDialog(null, "Asegurese de ingresar ingresar"
                   + "valores númericos correctos en los campos de datos");
           txtAproximacion.setText(null);
           txtTolerancia.setText(null);
           txtAproximacion.requestFocus();
           
       }

    }//GEN-LAST:event_btnCalcularActionPerformed

