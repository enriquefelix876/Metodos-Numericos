     /**
    * Este metodo evaluará un funcion en X
    * @param x
    * @return valor en x
    */
    public double evaluar(double x){ 
      
      //Editar esta función para obtener su raíz
      return 5*x-2;
    } 
    
    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed

       try{
       tablaSecante.setModel(modelo); 
       limpiarTabla(modelo);
       String[]datos=new String[6];
    
       //Se establece el primer intervalo
       double a = Double.parseDouble(txtA.getText());
       double fa = evaluar(a);
       
       //Se establece el segundo intervalo
       double b = Double.parseDouble(txtB.getText());
       double fb = evaluar(b);
       
       double c;
       
       //Se establece la tolerancia
       double tolerancia = 0.00000001;
        
        double d;
        double e;
        
        //Aqui se cambiarán los valoresde los intervalos en caso de que la 
        //pendiente sea negativa
        if (fa<fb) {
            d=a;
            e=b;
            a=e;
            b=d;
        }
        
        //Se realizará todo esto:
        do {  
            fa = evaluar(a);
            fb = evaluar(b);
           
           //Se realizará la siguiente formula para obtener el nuevo valor de c
           c = b- ((fb*(a-b))
                   /(fa-fb));

           datos[0]=String.valueOf(a); 
           datos[1]=String.valueOf(b);
           datos[2]=String.valueOf(evaluar(a));
           datos[3]=String.valueOf(evaluar(b));
           datos[4]=String.valueOf(c);
           datos[5]=String.valueOf(evaluar(c));

           //Si el valor de c evaluado en la función es menor a cero
           if (evaluar(c)<0) {
               //b obtendra el valor de c
               b = c;
               
           //En caso contrario     
           }else{
               //a obtendrá el valor de c
               a = c;
           }
           modelo.addRow(datos);
        //Mientras el valor absoluto de x evaluado en la función, sea mayor a la
        //tolerancia
        }while (Math.abs(evaluar(c))>tolerancia);

        txtRaiz.setText("Raiz: "+String.valueOf(c));
       
       }catch(Exception e){
       
           JOptionPane.showMessageDialog(null, "Asegurese de ingresar ingresar"
                   + "valores númericos correctos en los campos de datos");
           txtA.setText(null);
           txtB.setText(null);
           txtTolerancia.setText(null);
           txtA.requestFocus();
       }
       
    }//GEN-LAST:event_btnCalcularActionPerformed

     /**
     * Este metodo limpia las tablas de un modelo dado
     * @param modeloTabla a limpiar
     */
    private void limpiarTabla(DefaultTableModel modeloTabla){
    
        int filas = modeloTabla.getRowCount();
            
            for (int i = 0;filas>i; i++) {
                
                modeloTabla.removeRow(0);
            }
    }
    
