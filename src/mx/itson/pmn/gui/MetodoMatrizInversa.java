    /*
    Metodo encargado de mostrar las variables en pantalla dependiendo de la 
    cantidad
    */
    private void generarVariablesInversa(){
    
        switch (m){
        
            case 2:
                variablesInversa[0].setText("X");
                variablesInversa[1].setText("Y");
                variablesInversa[2].setText("X^-1");
                variablesInversa[3].setText("Y^-1");
                variablesInversa[4].setText(null);
                variablesInversa[5].setText(null);
                break;
                
            case 3:
                variablesInversa[0].setText("X");
                variablesInversa[1].setText("Y");
                variablesInversa[2].setText("Z");
                variablesInversa[3].setText("X^-1");
                variablesInversa[4].setText("Y^-1");
                variablesInversa[5].setText("Z^-1");
                break;
        
        }
    }
    
    /**
     * Metodo encargado de generar los campos necesarios para poder aplicar el
     * metodo de matriz inversa de acuerdo al número de variables
     */
    private void generarCamposInversa(){
    
        switch(m){
        
        case 2:
            for (int i = 0; i < 3; i++) {
            
            for (int j = 0; j < 6; j++) {
                
                camposInversa[i][j].setEnabled(false);
                camposInversa[i][j].setText(null);
            }
        }
        
            for (int i = 0; i < m; i++) {
            
                for (int j = 0; j < m*2; j++) {
                
                    camposInversa[i][j].setEnabled(true);
                    inversa02.setText("1");
                    inversa03.setText("0");
                    inversa12.setText("0");
                    inversa13.setText("1");
                    
                    inversa02.setEnabled(false);
                    inversa03.setEnabled(false);
                    inversa12.setEnabled(false);
                    inversa13.setEnabled(false);
                    
            }
        }    
                break;
                
        case 3:
            for (int i = 0; i < 3; i++) {
            
            for (int j = 0; j < 6; j++) {
                
                camposInversa[i][j].setEnabled(false);
                camposInversa[i][j].setText(null);
            }
        }
        
            for (int i = 0; i < m; i++) {
            
                for (int j = 0; j < m*2; j++) {
                
                    camposInversa[i][j].setEnabled(true);
                    inversa03.setText("1");
                    inversa04.setText("0");
                    inversa05.setText("0");
                    inversa13.setText("0");
                    inversa14.setText("1");
                    inversa15.setText("0");
                    inversa23.setText("0");
                    inversa24.setText("0");
                    inversa25.setText("1");
                    
                    inversa03.setEnabled(false);
                    inversa04.setEnabled(false);
                    inversa05.setEnabled(false);
                    inversa13.setEnabled(false);
                    inversa14.setEnabled(false);
                    inversa15.setEnabled(false);
                    inversa23.setEnabled(false);
                    inversa24.setEnabled(false);
                    inversa25.setEnabled(false);
                    
            }
        }    
                break;
        }   
    }

