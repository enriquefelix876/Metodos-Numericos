    double [][]sistemaInversa = new double[m][m*2];

    /**
     * Metodo encargado de generar la matriz inversa
     */
    private void generarMatrizInversa(){
        
        sistemaInversa = new double[m][m*2];
        
        for (int i = 0; i < m; i++) {
            
            for (int j = 0; j < m*2; j++) {
                
                sistemaInversa[i][j] = Double.parseDouble(camposInversa[i][j].getText());
            }
        }
    }
    
    String area="";
    
    /**
     * Evento encargado de generar el Método de Matriz Inversa
     * @param evt 
     */
    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed

        try{
        area = "";
        generarMatrizInversa();
        
        double divisor;
  
        int contador=0;
        
        do{
            
            divisor=sistemaInversa[contador][contador];
            
        //Divide la fila a analizar entre su diagonal para igualar el primer
        //valor a 1
        for (int i = 0; i < m*2; i++) {
            
            sistemaInversa[contador][i] =  sistemaInversa[contador][i]/divisor;
        }
        
        //Convertir a 0 las las columnas
        
            for (int i = 0; i < m; i++) {
                
                double producto = sistemaInversa[contador][contador]*sistemaInversa[i][contador];
                        
                for (int j = 0; j < m*2; j++) {
                    
                    if (i!=contador) {
                        
                        sistemaInversa[i][j] -= sistemaInversa[contador][j]*producto;
                    }
                }
            }
            
              //Mostrar matrices paso por paso en consola  
            for (int x=0; x < sistemaInversa.length; x++) {
                
                area+="|";
                txtArea.setText(area);
                
            for (int y=0; y < sistemaInversa[x].length; y++) {
                
                area+=String.format("%.2f", sistemaInversa[x][y]);
                txtArea.setText(area);
                
            if (y!=sistemaInversa[x].length-1) 
                
                area+="\t";
                txtArea.setText(area);
            }
            area+="|"+"\n";
            txtArea.setText(area);
            }

        contador++;
        area+="________________________________"+"\n";
        txtArea.setText(area);
        
        }while(contador<m);
        
        area+="________________________________"+"\n";
        txtArea.setText(area);
        
        }catch(Exception e){
        
            JOptionPane.showMessageDialog(null, "Asegurese de ingresar valores "
                    + "en todos los campos con el formato correcto");
        }
    }//GEN-LAST:event_btnCalcularActionPerformed

