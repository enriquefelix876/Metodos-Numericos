    /**
     * Evento encargado de generar Paso a Paso el sistema de ecuaciones por el 
     * metodo de Gauss Jordan
     * @param evt del botón Calcular
     */
    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        
        try{
        area="";
        generarMatriz();
        
        double divisor;
  
        int contador=0;
        
        do{
            
            divisor=sistema[contador][contador];
            
        //Divide la fila a analizar entre su diagonal para igualar el primer
        //valor a 1
        for (int i = 0; i < n+1; i++) {
            
            sistema[contador][i] =  sistema[contador][i]/divisor;
        }
        
        //Convertir a 0 las las columnas
        
            for (int i = 0; i < n; i++) {
                
                double producto = sistema[contador][contador]*sistema[i][contador];
                        
                for (int j = 0; j < n+1; j++) {
                    
                    if (i!=contador) {
                        
                        sistema[i][j] -= sistema[contador][j]*producto;
                    }
                }
            }

              //Mostrar matrices paso por paso en TextArea  
            for (int x=0; x < sistema.length; x++) {
                
                area+= "|";
                txtArea.setText(area);
                
            for (int y=0; y < sistema[x].length; y++) {
                
                area+=String.format("%.2f", sistema[x][y]);
                txtArea.setText(area);
                
            if (y!=sistema[x].length-1) 
                
                area+="\t";
                txtArea.setText(area);
            }
            area+="|"+"\n";
            txtArea.setText(area);
            }

        contador++;
        area+="________________________________"+"\n";
        txtArea.setText(area);
        
        }while(contador<n);

        area+="________________________________"+"\n";
        txtArea.setText(area);
        
        }catch(Exception e){
        
            JOptionPane.showMessageDialog(null, "Asegurese de ingresar valores "
                    + "en todos los campos con el formato correcto");
        }

    }//GEN-LAST:event_btnCalcularActionPerformed

