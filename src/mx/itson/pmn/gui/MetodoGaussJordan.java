    /**
     * Metodo encargado de generar la matriz de con los valores obtenidos de los
     * campos de texto
     */
    private void generarMatriz(){
        
        sistema = new double[n][n+1];
        
        for (int i = 0; i < n; i++) {
            
            for (int j = 0; j < n+1; j++) {
                
                sistema[i][j] = Double.parseDouble(campos[i][j].getText());
            }
        }
    }
