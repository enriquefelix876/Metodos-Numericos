    private void btnResolverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnResolverActionPerformed

        //Bloque try en caso de que suceda alguna excepción
        try {
            generarArreglosDeVariables();
            generarArregloXAlCuadrado();
            generarArregloX2AlCuadrado();
            generarArreglosProductos();

            double sumatoriax1 = sumatoriaArreglo(valorDeX);
            double sumatoriax2 = sumatoriaArreglo(valorDeX2);
            double sumatoriay = sumatoriaArreglo(valorDeY);
            double sumatoriaXAlCuadrado = sumatoriaArreglo(xAlCuadrado);
            double sumatoriaX2AlCuadrado = sumatoriaArreglo(x2AlCuadrado);
            double sumatoriax1Porx2 = sumatoriaArreglo(productox1Porx2);
            double sumatoriax1PorY = sumatoriaArreglo(productox1PorY);
            double sumatoriax2PorY = sumatoriaArreglo(productox2PorY);

            //Valores de la tabla 1 para evitar campos duplicados
            limpiarTabla(modelo);

            //Se ingresan los valores sumatoriaX, sumatoriaY, sumatoriaXAlCuadrado,
            //sumatoriaProductoXPorY, promedioX, promedioY y n
            //redondeados a 2 cifras en la tabla 1
            String[]valoresTabla = new String[8];
            valoresTabla[0] = String.valueOf(n);
            valoresTabla[1] = String.valueOf(String.format("%.2f", sumatoriax1));
            valoresTabla[2] = String.valueOf(String.format("%.2f", sumatoriax2));
            valoresTabla[3] = String.valueOf(String.format("%.2f", sumatoriay));
            valoresTabla[4] = String.valueOf(String.format("%.2f", sumatoriaXAlCuadrado));
            valoresTabla[5] = String.valueOf(String.format("%.2f", sumatoriax1Porx2));
            valoresTabla[6] = String.valueOf(String.format("%.2f", sumatoriax1PorY));
            valoresTabla[7] = String.valueOf(String.format("%.2f", sumatoriax2PorY));

            modelo.addRow(valoresTabla);

            matriz = generarMatriz(sumatoriax1, sumatoriax2, sumatoriay, sumatoriaXAlCuadrado,
                sumatoriax1Porx2, sumatoriax1PorY, sumatoriaX2AlCuadrado, sumatoriax2PorY);

            //Aplicación del metodo de Gauss Jordan
            double divisor;

            int contador=0;

            do{

                divisor = matriz[contador][contador];

                //Divide la fila a analizar entre su diagonal para igualar el primer
                //valor a 1
                for (int i = 0; i < 4; i++) {

                    matriz[contador][i] =  matriz[contador][i]/divisor;
                }

                //Convertir a 0 las las columnas

                for (int i = 0; i < 3; i++) {

                    double producto = matriz[contador][contador]*matriz[i][contador];

                    for (int j = 0; j < 4; j++) {

                        if (i!=contador) {

                            matriz[i][j] -= matriz[contador][j]*producto;
                        }
                    }
                }

                /**
                * Se muestra en el programa el valor de Y en función de la ecuación
                * generada por el metodo de regresión Lineal Multiple
                */
                txtEcuacionDeLaRecta.setText("Y = "+String.format("%.2f", matriz[2][3])
                    +"x2+"+String.format("%.2f", matriz[1][3])+"x1+"+String.format("%.2f", matriz[0][3]));

                contador++;
                //Se limpian los valores de la tabla 2
                limpiarTabla(modelo2);

                /**
                * Se ingresan en la tabla 2, los valores de Y ingresados
                * previamente y las aproximaciones en función de la ecuación
                * generada por el metodo de regresión Lineal Multiple
                */
                String[]valoresTabla2 = new String[2];

                for (int i = 0; i < n; i++) {

                    valoresTabla2[0]=String.valueOf(String.format("%.2f", valorDeY[i]));

                    valoresTabla2[1]= String.valueOf(String.format("%.2f",(matriz[2][3]*valorDeX2[i])+
                        (matriz[1][3]*valorDeX[i])+
                        (matriz[0][3])));
                modelo2.addRow(valoresTabla2);
            }

        }while(contador<n);

        }catch(Exception e){

            generarGrafica();
        }
        generarGrafica();
    }//GEN-LAST:event_btnResolverActionPerformed

       /**
     * Este metodo generá un gráfica de las aproximaciones de Y usando la 
     * Libreria JFreeChart al dar click en el botón Aceptar
     */
    private void generarGrafica(){
    
        DefaultCategoryDataset dataset = new DefaultCategoryDataset();

        //Se van agregandos los elementos de Y en funcion de la función obtenida
        //por el metodo de regresión lineal multiple
        for (int i = 0; i < n; i++) {

            //Puntos dados previamente
            dataset.addValue(((matriz[2][3]*i)+
                (matriz[1][3]*i)+
                (matriz[0][3])), "Aproximación generada por Regresión Lineal Multiple", "" + i);

        }

        JFreeChart chart = ChartFactory.createLineChart(
                "Grafica Regresión Lineal Multiple",
                "X1",
                "Y",
                dataset,
                PlotOrientation.VERTICAL,
                true,
                false,
                false
        );

        //Mostramos la grafica en pantalla
        ChartPanel panel = new ChartPanel(chart);        
        panelGrafica.setLayout(new java.awt.BorderLayout());
        panelGrafica.add(panel);   
        panelGrafica.validate();
    }
    
