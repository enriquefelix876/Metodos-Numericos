    /**
     * Este metodo se encarga de generar la matriz en la que se aplicará el
     * método de Gauss Jordan para obtener los valores de a2, a1 y a
     * @param sumatoriax1
     * @param sumatoriax2
     * @param sumatoriaY
     * @param sumatoriax1AlCuadrado
     * @param sumatoriax1PorX2
     * @param sumatoriax1PorY
     * @param sumatoriax2AlCuadrado
     * @param sumatoriax2PorY
     * @return 
     */
private double[][]generarMatriz(double sumatoriax1, double sumatoriax2, double sumatoriaY,
        double sumatoriax1AlCuadrado, double sumatoriax1PorX2, double sumatoriax1PorY,
        double sumatoriax2AlCuadrado, double sumatoriax2PorY){

    double[][]sistema=new double [3][4];
    
    n = Integer.parseInt(comboCantidad.getSelectedItem().toString());
    
    sistema[0][0] = n;
    sistema[0][1] = sumatoriax1;
    sistema[0][2] = sumatoriax2;
    sistema[0][3] = sumatoriaY;
    
    sistema[1][0] = sumatoriax1;
    sistema[1][1] = sumatoriax1AlCuadrado;
    sistema[1][2] = sumatoriax1PorX2;
    sistema[1][3] = sumatoriax1PorY;
    
    sistema[2][0] = sumatoriax2;
    sistema[2][1] = sumatoriax1PorX2;
    sistema[2][2] = sumatoriax2AlCuadrado;
    sistema[2][3] = sumatoriax2PorY;
    
    return sistema;
}

