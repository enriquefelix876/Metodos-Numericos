    /**
     * Este metodo es el encargado de calcular el valor de f en función de x
     * @param x 
     * @return un dato double de f en función de x
     */
    public static double f(double x) {
        
        //Aqui se edita la función para ingresar una nueva
        return 5*x-2;
    }
    
    /**
     * Este metodo limpia las tablas de un modelo dado
     * @param modeloTabla a limpiar
     */
    private void limpiarTabla(DefaultTableModel modeloTabla){
    
        int filas = modeloTabla.getRowCount();
            
            for (int i = 0;filas>i; i++) {
                
                modeloTabla.removeRow(0);
            }
    }

