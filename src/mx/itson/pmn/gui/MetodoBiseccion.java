    /**
     * Este evento sirve para calcular la raíz de una función, dado sus
     * intervalos y la tolerancia dada, mediante el método de Bisección
     * @param evt del evento Calcular
     */
    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed

        try{
        //Se invoca el metodo para limpiar los datos de una tabla para evitar
        //registros duplicados
        limpiarTabla(modelo);
        
        //Se le define el modelo a la tabla de datos
        tablaBiseccion.setModel(modelo);
        
        //Se crea el arreglo de datos de la tabla
        String datos [] = new String[4];

        //Aqui se especifica el primer intervalo y el segundo
        a = Double.parseDouble(txtA.getText());
        b = Double.parseDouble(txtB.getText());
        double c = 0;
        
        //Se inician las iteracciones en 0
        iteracciones = 0;

        if (f(a) * f(b) > 0) {
            return;

        }
        
        //Se realizan las iteracciones mientras...
        while (true) {
            
            //Se define a c como el promedio de a y b
            c = (a + b) / 2;

            //El valor de c sea menor a la tolerancia
            if (Math.abs(f(c)) <= tolerancia) {
                
                //Se escribe el área en el JLabel
                txtRaizBiseccion.setText(String.format("%.3f", c));
                return;
            }
            
            //Si f(a) por f(c) es menor a 0
            if (f(a) * f(c) < 0) {
                
                //b se iguala a c
                b = c;
                
                //Sino
            } else {
                
                //a se iguala a c
                a = c;
            }
            
            //Se ingresan los datos en cada iteracción en una fila de la tabla
            datos[0] = String.valueOf(iteracciones);
            datos[1] = String.format("%.10f", a);
            datos[2] = String.format("%.10f", b);
            datos[3] = String.format("%.10f", a-b);
            iteracciones++;

            modelo.addRow(datos);
        }
        }catch(Exception e){
        
            JOptionPane.showMessageDialog(null, "Asegurese de escribir valores"
                    + "númericos en los campos de texto");
            txtA.setText(null);
            txtB.setText(null);
            txtTolerancia.setText(null);
            txtA.requestFocus();
        }
    }//GEN-LAST:event_btnCalcularActionPerformed

