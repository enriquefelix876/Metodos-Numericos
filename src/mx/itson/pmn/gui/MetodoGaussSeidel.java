    /**
     * Este método es el encargado de limpiar los campos para realizar una 
     * nueva operación
     */
    private void limpiarCampos(){
    
        for (int i = 0; i < n; i++) {
            
            for (int j = 0; j < n+1; j++) {
                
                campos[i][j].setText(null);
            }
        }
    }
    
    /**
     * Este metodo crea una matriz double con los los valores ingresados en los 
     * campos de texto
     */
    private void generarMatriz(){
        
        sistema = new double[n][n+1];
        
        try {
            for (int i = 0; i < n; i++) {
            
            for (int j = 0; j < n+1; j++) {
                
                sistema[i][j] = Double.parseDouble(campos[i][j].getText());
            }
        }
        } catch (NumberFormatException e) {
            
            JOptionPane.showMessageDialog(null, "Asegurate de ingresar "
                    + "solamente valores númericos");
            limpiarCampos();
        }
        
    }
    
